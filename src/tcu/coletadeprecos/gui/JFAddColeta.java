    private void jButton_ItemUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ItemUpActionPerformed
        empresa.setItem(empresa.getItem() + 1);
        jText_Item.setText(String.valueOf(empresa.getItem()));
    }//GEN-LAST:event_jButton_ItemUpActionPerformed

    private void jButton_ItemDownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ItemDownActionPerformed
        if (empresa.getItem() > 1){
            empresa.setItem(empresa.getItem() - 1);
            jText_Item.setText(String.valueOf(empresa.getItem()));
        }else{
            empresa.setItem(1);
            jText_Item.setText(String.valueOf(empresa.getItem()));
        }
        
    }//GEN-LAST:event_jButton_ItemDownActionPerformed

    private void jButton_QtdDownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_QtdDownActionPerformed
        if (empresa.getQtd()> 1){
            empresa.setQtd(empresa.getQtd()- 1);
            jText_Qtd.setText(String.valueOf(empresa.getQtd()));
        }else{
            empresa.setQtd(1);
            jText_Qtd.setText(String.valueOf(empresa.getQtd()));
        }
    }//GEN-LAST:event_jButton_QtdDownActionPerformed

    private void jButton_QtdUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_QtdUpActionPerformed
        empresa.setQtd(empresa.getQtd() + 1);
        jText_Qtd.setText(String.valueOf(empresa.getQtd()));
    }//GEN-LAST:event_jButton_QtdUpActionPerformed

