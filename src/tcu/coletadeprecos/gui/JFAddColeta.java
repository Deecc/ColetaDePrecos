    private void jButton_LimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LimparActionPerformed
       int option = JOptionPane.showConfirmDialog(null ,"Tem certeza que deseja limpar todos os campos?","Limpar",JOptionPane.OK_CANCEL_OPTION);
       if (option == 0) {
        jText_Empresa.setText("");
        jText_Desc.setText("");
        jText_Item.setText(String.valueOf((new Empresa().getItem())));
        jText_Desc.setText("");
        jText_VlrUnitario.setText("");
        jText_Total.setText("");
       }
    }//GEN-LAST:event_jButton_LimparActionPerformed

